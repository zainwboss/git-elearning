<script>
	$(document).ready(function() {
		CheckLogin();
	});

	function CheckLogin() {
		$.ajax({
			type: "POST",
			url: "ajax/index_login/CheckLogin.php",
			dataType: "Json",
			success: function(data) {
				if (data.change_password_status == 0 && data.result == 1) {
					ModalSetPassword();
				} else if (data.result == 0) {
					location.href = "../";
				}
			}
		});
	}

	function ModalSetPassword() {
		$.ajax({
			url: "ajax/index_login/ModalSetPassword.php",
			dataType: "html",
			success: function(response) {
				$('#modal_pro').modal({
					backdrop: 'static',
					keyboard: false,
					show: false,
				});
				$("#show_modal_pro").html(response);
				$("#modal_pro").modal('show');
				KTApp.init();
				KTPasswordMeter.createInstances();
			}
		});
	}

	function ModalPassword() {
		$.ajax({
			url: "ajax/index_login/ModalPassword.php",
			dataType: "html",
			success: function(response) {
				$("#show_modal_pro").html(response);
				$("#modal_pro").modal('show');
				KTApp.init();
				KTPasswordMeter.createInstances();
			}
		});
	}

	function Logout() {
		Swal.fire({
			title: 'กรุณายืนยันออกจากระบบ',
			icon: "question",
			iconColor: '#c0a4f7',
			buttonsStyling: false,
			showCancelButton: true,
			confirmButtonText: "ยืนยัน",
			cancelButtonText: 'ยกเลิก',
			customClass: {
				confirmButton: "btn btn-sm btn-info btn-hover-scale",
				cancelButton: 'btn btn-sm btn-light btn-hover-scale'
			}
		}).then((result) => {
			if (result.isConfirmed) {
				window.location.href = 'logout.php';
			}
		});
	}
</script>

